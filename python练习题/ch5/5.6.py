#人生不同阶段

# 设置年龄变量
age = 5  

# 根据年龄判断人生阶段
if age < 2:
    print("This person is a baby.")
elif age <= 4:
    print("This person is a toddler.")
elif age <= 13:
    print("This person is a child.")
elif age <= 18:
    print("This person is a teenager.")
elif age <= 65:
    print("This person is an adult.")
else:
    print("This person is a senior.")